// Emits fallbacks.css from theme.css (TECH-06): hex-first declarations for
// pre-Chromium-111 Thai Android WebViews, then an @supports block restating
// the OKLCH values for modern browsers. Source of truth stays theme.css —
// every `--token: oklch(...) /* #hex */;` pair is parsed from it.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = dirname(fileURLToPath(import.meta.url));
const theme = readFileSync(join(root, "../theme.css"), "utf8");

const TOKEN_RE = /(--[\w-]+):\s*(oklch\([^)]*\))\s*\/\*\s*(#[0-9a-fA-F]{6})\s*\*\//g;

const light = [];
const modern = [];
for (const match of theme.matchAll(TOKEN_RE)) {
  const [, name, oklch, hex] = match;
  // Only top-level @theme tokens get a static fallback; dark overrides degrade
  // to light on legacy WebViews (acceptable — they predate dark-mode media too).
  if (theme.indexOf(match[0]) > theme.indexOf(':root[data-theme="dark"]')) continue;
  light.push(`  ${name}: ${hex};`);
  modern.push(`    ${name}: ${oklch};`);
}

const output = `/* GENERATED by scripts/emit-fallbacks.mjs — do not edit. Source: theme.css */
:root {
${light.join("\n")}
}
@supports (color: oklch(0 0 0)) {
  :root {
${modern.join("\n")}
  }
}
`;

if (light.length === 0) {
  // A swapped-in candidate whose tokens don't match `oklch(...) /* #hex */`
  // would silently ship NO legacy-WebView fallbacks (TECH-06) — fail loudly.
  console.error(
    "emit-fallbacks: 0 tokens matched — the swapped theme.css doesn't follow the `--token: oklch(...) /* #hex */;` format. Fix the candidate block before shipping.",
  );
  process.exit(1);
}
writeFileSync(join(root, "../fallbacks.css"), output);
console.log(`fallbacks.css: ${light.length} tokens emitted (hex-first + @supports oklch)`);
